from __future__ import annotations

from dataclasses import dataclass, field
from enum import StrEnum
from typing import Any

from deerflow.skills.catalog import SkillCatalog


class PlanStepKind(StrEnum):
    """Kind of step in an execution plan."""

    sequence = "sequence"
    parallel = "parallel"
    gate = "gate"


@dataclass
class ExecutionStep:
    """A single step in an execution plan.

    Attributes:
        skill: Skill name to execute. Empty for gate/parallel steps.
        channel: Channel/blueprint to use (e.g. ``"full"``, ``"analysis"``).
        description: Human-readable description of this step.
        kind: Step kind (sequence, parallel, gate).
        params: Optional parameters for the step.
    """

    skill: str
    channel: str = "standard"
    description: str = ""
    kind: PlanStepKind = PlanStepKind.sequence
    params: dict[str, Any] = field(default_factory=dict)

    @property
    def is_gate(self) -> bool:
        return self.kind == PlanStepKind.gate

    @staticmethod
    def gate(description: str = "Human review required") -> ExecutionStep:
        return ExecutionStep(
            skill="",
            channel="",
            description=description,
            kind=PlanStepKind.gate,
        )

    @staticmethod
    def parallel(description: str = "Parallel execution", steps: list[ExecutionStep] | None = None) -> ExecutionStep:
        return ExecutionStep(
            skill="",
            channel="",
            description=description,
            kind=PlanStepKind.parallel,
            params={"steps": steps or []},
        )


@dataclass
class ExecutionPlan:
    """An ordered list of execution steps generated by the orchestrator.

    Attributes:
        steps: Ordered list of execution steps.
        metadata: Optional metadata about the plan (confidence, reasoning, etc.).
    """

    steps: list[ExecutionStep]
    metadata: dict[str, Any] = field(default_factory=dict)

    @property
    def total_steps(self) -> int:
        return len(self.steps)

    def has_next(self, index: int) -> bool:
        return index < self.total_steps - 1


